const result = {};
let ok = 0, skip = 0;

for (const [code, nameFr, nameAr, nameEn, coords] of WILAYAS) {
  if (!coords || coords.length < 3) { skip++; continue; }

  // Validate all coords are within Algeria bounds
  const bad = coords.filter(([lon, lat]) =>
    lon < LON_MIN - 0.1 || lon > LON_MAX + 0.1 ||
    lat < LAT_MIN - 0.1 || lat > LAT_MAX + 0.1
  );
  if (bad.length > 0) {
    console.log(`WARNING ${code} ${nameFr}: out-of-bounds coords:`, bad);
  }

  const path = polyPath(coords);

  // Centroid = average of coords
  const cx = Math.round(coords.reduce((s, p) => s + toX(p[0]), 0) / coords.length);
  const cy = Math.round(coords.reduce((s, p) => s + toY(p[1]), 0) / coords.length);

  // Validate SVG coords
  const nums = path.match(/-?\d+\.?\d*/g).map(Number);
  const minV = Math.min(...nums), maxV = Math.max(...nums);
  if (minV < -5 || maxV > 570) {
    console.log(`BAD PATH ${code} ${nameFr}: min=${minV.toFixed(0)} max=${maxV.toFixed(0)}`);
    skip++;
    continue;
  }

  result[code] = { path, cx, cy, nameFr, nameAr, nameEn };
  ok++;
}

console.log(`\n✅ Generated: ${ok} wilayas, skipped: ${skip}`);

// Verify key wilayas
for (const c of ['16','31','25','11','37','01','09']) {
  const w = result[c];
  console.log(`  ${c} ${(w?.nameFr||'MISSING').padEnd(20)} cx=${w?.cx} cy=${w?.cy}`);
}

const ts = `// Auto-generated — Linear projection (no d3-geo artifacts), Algeria bounds, viewBox 0 0 480 560
export interface WilayaPath { path:string; cx:number; cy:number; nameFr:string; nameAr:string; nameEn:string; }
export const WILAYA_PATHS: Record<string,WilayaPath> = ${JSON.stringify(result, null, 2)};
`;

